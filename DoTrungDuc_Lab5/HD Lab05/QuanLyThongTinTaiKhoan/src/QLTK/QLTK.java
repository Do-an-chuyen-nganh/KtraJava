    private Account GetAccount() throws Exception
    {
        if("".equals(txtAccount.getText()) || "".equals(txtAccountName.getText()) || "".equals(txtAddress.getText()) )
        {
           throw new Exception("Thông tin tài khoản không được để trống!”");
        }
        return new Account(txtAccount.getText(),txtAccountName.getText(), txtAddress.getText(),Double.parseDouble(txtAmount.getText()) );
    }
    
