    private void BindAccount() throws Exception
    {
        DefaultTableModel model = (DefaultTableModel)jtableAccount.getModel();
        model.setRowCount(0); //remove all rows
        ArrayList<Account> listAccount = Account.GetListAccount();  
        double totalAmount = 0; 
        for (int i = 0; i < listAccount.size(); i++) 
            {
                Account temp = listAccount.get(i);
                model.addRow(new Object[]{ (i+1) , temp.AccountNo  , temp.AccountName, temp.Address , temp.Amount}); 
                totalAmount += temp.Amount;
            }
        txtTotal.setText( totalAmount + "");
    }
    
